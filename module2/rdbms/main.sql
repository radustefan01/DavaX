CREATE TABLE offices
(
    office_id NUMBER NOT NULL,

    city VARCHAR2(50) NOT NULL,

    address VARCHAR2(200),

    CONSTRAINT pk_offices
        PRIMARY KEY (office_id),

    CONSTRAINT uq_office_city
        UNIQUE(city)
);

CREATE TABLE app_users (
    user_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    full_name VARCHAR2(100) NOT NULL,
    email VARCHAR2(255) NOT NULL,
    role_code VARCHAR2(20) NOT NULL,
    line_manager_id NUMBER,
    is_active NUMBER(1) DEFAULT 1 NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    office_id NUMBER NOT NULL,

	CONSTRAINT fk_user_office
	FOREIGN KEY (office_id)
	REFERENCES offices(office_id),
    

    CONSTRAINT pk_app_users
        PRIMARY KEY (user_id),

    CONSTRAINT uq_app_users_email
        UNIQUE (email),

    CONSTRAINT chk_app_users_role
        CHECK (role_code IN ('EMPLOYEE', 'LINE_MANAGER', 'HR', 'ADMIN')),

    CONSTRAINT chk_app_users_active
        CHECK (is_active IN (0, 1)),

    CONSTRAINT chk_employee_has_manager
        CHECK (
            role_code <> 'EMPLOYEE'
            OR line_manager_id IS NOT NULL
        ),

    CONSTRAINT fk_app_users_line_manager
        FOREIGN KEY (line_manager_id)
        REFERENCES app_users(user_id)
);

CREATE TABLE timesheets (
    timesheet_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    employee_id NUMBER NOT NULL,
    week_start_date DATE NOT NULL,

    monday_hours NUMBER(4, 2) DEFAULT 0 NOT NULL,
    tuesday_hours NUMBER(4, 2) DEFAULT 0 NOT NULL,
    wednesday_hours NUMBER(4, 2) DEFAULT 0 NOT NULL,
    thursday_hours NUMBER(4, 2) DEFAULT 0 NOT NULL,
    friday_hours NUMBER(4, 2) DEFAULT 0 NOT NULL,

    monday_place VARCHAR2(10) DEFAULT 'REMOTE' NOT NULL,
    tuesday_place VARCHAR2(10) DEFAULT 'REMOTE' NOT NULL,
    wednesday_place VARCHAR2(10) DEFAULT 'REMOTE' NOT NULL,
    thursday_place VARCHAR2(10) DEFAULT 'REMOTE' NOT NULL,
    friday_place VARCHAR2(10) DEFAULT 'REMOTE' NOT NULL,

    status VARCHAR2(20) DEFAULT 'DRAFT' NOT NULL,
    submitted_at TIMESTAMP,
    reviewed_by NUMBER,
    reviewed_at TIMESTAMP,
    rejection_reason VARCHAR2(500),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    work_details CLOB,
    office_id NUMBER NOT NULL,

	CONSTRAINT fk_timesheet_office
	FOREIGN KEY (office_id)
	REFERENCES offices(office_id),

    CONSTRAINT chk_work_details_json
        CHECK (work_details IS JSON),

    CONSTRAINT pk_timesheets
        PRIMARY KEY (timesheet_id),

    CONSTRAINT uq_timesheets_employee_week
        UNIQUE (employee_id, week_start_date),

    CONSTRAINT fk_timesheets_employee
        FOREIGN KEY (employee_id)
        REFERENCES app_users(user_id),

    CONSTRAINT fk_timesheets_reviewed_by
        FOREIGN KEY (reviewed_by)
        REFERENCES app_users(user_id),

    CONSTRAINT chk_timesheet_status
        CHECK (status IN ('DRAFT', 'SUBMITTED', 'AUTO_REJECTED', 'APPROVED', 'REJECTED')),

    CONSTRAINT chk_timesheet_hours
        CHECK (
            monday_hours BETWEEN 0 AND 24
            AND tuesday_hours BETWEEN 0 AND 24
            AND wednesday_hours BETWEEN 0 AND 24
            AND thursday_hours BETWEEN 0 AND 24
            AND friday_hours BETWEEN 0 AND 24
        ),

    CONSTRAINT chk_timesheet_places
        CHECK (
            monday_place IN ('REMOTE', 'OFFICE')
            AND tuesday_place IN ('REMOTE', 'OFFICE')
            AND wednesday_place IN ('REMOTE', 'OFFICE')
            AND thursday_place IN ('REMOTE', 'OFFICE')
            AND friday_place IN ('REMOTE', 'OFFICE')
        ),

    CONSTRAINT chk_timesheet_review_fields
        CHECK (
            status NOT IN ('APPROVED', 'REJECTED')
            OR (reviewed_by IS NOT NULL AND reviewed_at IS NOT NULL)
        ),

    CONSTRAINT chk_timesheet_rejection_reason
        CHECK (
            status NOT IN ('AUTO_REJECTED', 'REJECTED')
            OR rejection_reason IS NOT NULL
        )
