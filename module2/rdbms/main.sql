CREATE OR REPLACE PROCEDURE submit_timesheet (
    p_timesheet_id IN timesheets.timesheet_id%TYPE,
    p_employee_id IN app_users.user_id%TYPE
)
IS
    v_count NUMBER;
BEGIN
    -- Checks whether the specified timesheet belongs to the employee and can be submitted.
    -- Checks that the submitted timesheet belongs to an active line manager.
    -- Checks that the submitted timesheet belongs to an active line manager.

    -- Counts eligible timesheets belonging to the specified employee.
    SELECT COUNT(*)
    INTO v_count
    FROM timesheets
    WHERE timesheet_id = p_timesheet_id
      AND employee_id = p_employee_id
      AND status IN ('DRAFT', 'AUTO_REJECTED', 'REJECTED');

    IF v_count = 0 THEN
        RAISE_APPLICATION_ERROR(
            -20001,
            'Timesheet does not belong to this employee or cannot be submitted.'
        );
    END IF;

    UPDATE timesheets
    SET status = 'SUBMITTED'
    WHERE timesheet_id = p_timesheet_id;
END;
/

CREATE OR REPLACE PROCEDURE reject_timesheet (
    p_timesheet_id IN timesheets.timesheet_id%TYPE,
    p_manager_id IN app_users.user_id%TYPE,
    p_rejection_reason IN timesheets.rejection_reason%TYPE
)
IS
    v_count NUMBER;
BEGIN
    IF p_rejection_reason IS NULL THEN
        RAISE_APPLICATION_ERROR(-20003, 'A rejection reason is required.');
    END IF;

    -- Verifies that the timesheet is submitted, belongs to the manager's employee,
    -- and that the manager is an active line manager.
    SELECT COUNT(*)
    INTO v_count
    FROM timesheets t
    JOIN app_users employee
        ON employee.user_id = t.employee_id
    JOIN app_users manager_user
        ON manager_user.user_id = p_manager_id
    WHERE t.timesheet_id = p_timesheet_id
      AND t.status = 'SUBMITTED'
      AND employee.line_manager_id = p_manager_id
      AND manager_user.role_code = 'LINE_MANAGER'
      AND manager_user.is_active = 1;

    IF v_count = 0 THEN
        RAISE_APPLICATION_ERROR(
            -20004,
            'Timesheet is not submitted or does not belong to this line manager.'
        );
    END IF;

    UPDATE timesheets
    SET status = 'REJECTED',
        reviewed_by = p_manager_id,
        reviewed_at = SYSTIMESTAMP,
        rejection_reason = p_rejection_reason
    WHERE timesheet_id = p_timesheet_id;
END;
/

CREATE OR REPLACE PROCEDURE approve_timesheet (
    p_timesheet_id IN timesheets.timesheet_id%TYPE,
    p_manager_id IN app_users.user_id%TYPE
)
IS
    v_count NUMBER;
BEGIN
    -- Verifies that the submitted timesheet belongs to an employee managed
    -- by the specified active line manager.
    SELECT COUNT(*)
    INTO v_count
    FROM timesheets t
    JOIN app_users employee
        ON employee.user_id = t.employee_id
    JOIN app_users manager_user
        ON manager_user.user_id = p_manager_id
    WHERE t.timesheet_id = p_timesheet_id
      AND t.status = 'SUBMITTED'
      AND employee.line_manager_id = p_manager_id
      AND manager_user.role_code = 'LINE_MANAGER'
      AND manager_user.is_active = 1;

    IF v_count = 0 THEN
        RAISE_APPLICATION_ERROR(
            -20002,
            'Timesheet is not submitted or does not belong to this line manager.'
        );
    END IF;

    UPDATE timesheets
    SET status = 'APPROVED',
        reviewed_by = p_manager_id,
        reviewed_at = SYSTIMESTAMP,
        rejection_reason = NULL
    WHERE timesheet_id = p_timesheet_id;
END;
/


CREATE OR REPLACE VIEW v_timesheet_overview AS
SELECT
    t.timesheet_id,
    t.employee_id,
    employee.full_name AS employee_name,
    employee.email AS employee_email,
    employee.line_manager_id,
    manager_user.full_name AS line_manager_name,
    t.week_start_date,
    t.status,
    (
          t.monday_hours
        + t.tuesday_hours
        + t.wednesday_hours
        + t.thursday_hours
        + t.friday_hours
    ) AS total_hours,
    (
          CASE WHEN t.monday_place = 'OFFICE' AND t.monday_hours > 0 THEN 1 ELSE 0 END
        + CASE WHEN t.tuesday_place = 'OFFICE' AND t.tuesday_hours > 0 THEN 1 ELSE 0 END
        + CASE WHEN t.wednesday_place = 'OFFICE' AND t.wednesday_hours > 0 THEN 1 ELSE 0 END
        + CASE WHEN t.thursday_place = 'OFFICE' AND t.thursday_hours > 0 THEN 1 ELSE 0 END
        + CASE WHEN t.friday_place = 'OFFICE' AND t.friday_hours > 0 THEN 1 ELSE 0 END
    ) AS office_days,
    t.submitted_at,
    t.reviewed_by,
    reviewer.full_name AS reviewed_by_name,
    o.city,
    t.reviewed_at,
    t.rejection_reason
FROM timesheets t
JOIN app_users employee
    ON employee.user_id = t.employee_id
JOIN offices o
    ON o.office_id = employee.office_id
LEFT JOIN app_users manager_user
    ON manager_user.user_id = employee.line_manager_id
LEFT JOIN app_users reviewer
    ON reviewer.user_id = t.reviewed_by;


CREATE MATERIALIZED VIEW mv_employee_weekly_hours
BUILD IMMEDIATE
REFRESH COMPLETE ON DEMAND
AS
SELECT
    employee_id,
    COUNT(*) AS total_timesheets,
    SUM(
          monday_hours
        + tuesday_hours
        + wednesday_hours
        + thursday_hours
        + friday_hours
    ) AS total_hours
FROM timesheets
GROUP BY employee_id;


-- Displays the total approved working hours for every employee.
SELECT
    employee_id,
    SUM(
          monday_hours
        + tuesday_hours
        + wednesday_hours
        + thursday_hours
        + friday_hours
    ) AS total_hours
FROM timesheets
WHERE status = 'APPROVED'
GROUP BY employee_id;

-- Lists every employee together with their line manager.
-- Employees without a manager are also displayed.
SELECT
    e.full_name AS employee_name,
    m.full_name AS manager_name
FROM app_users e
LEFT JOIN app_users m
    ON e.line_manager_id = m.user_id;


-- Ranks employees by their total approved worked hours.
SELECT
    employee_id,
    SUM(
          monday_hours
        + tuesday_hours
        + wednesday_hours
        + thursday_hours
        + friday_hours
    ) AS total_hours,
    RANK() OVER (
        ORDER BY SUM(
              monday_hours
            + tuesday_hours
            + wednesday_hours
            + thursday_hours
            + friday_hours
        ) DESC
    ) AS employee_rank
FROM timesheets
WHERE status = 'APPROVED'
GROUP BY employee_id;
