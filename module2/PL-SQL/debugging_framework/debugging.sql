CREATE OR REPLACE PROCEDURE adjust_salaries_by_commission IS

    CURSOR employee_cursor IS
        SELECT employee_id, full_name, salary, commission_pct
        FROM employees
        ORDER BY employee_id;
    v_increase NUMBER;
BEGIN
    debug_utils.log( $$PLSQL_UNIT,  $$PLSQL_LINE, 'Procedure started');

    -- go through all employees
    FOR employee_row IN employee_cursor LOOP

        debug_utils.log( $$PLSQL_UNIT,  $$PLSQL_LINE, 'Reading employee: ' || employee_row.full_name);

        IF employee_row.commission_pct IS NULL THEN
            debug_utils.log($$PLSQL_UNIT, $$PLSQL_LINE, 'Commission is NULL; using the default rate of 2%', 'WARN');
        END IF;
    

        debug_utils.log_msg('testtttttttttttttttttttttttttttttttt');
        debug_utils.log_msg('testtttttttttttttttttttttttttttttttt');
        debug_utils.log_msg('testtttttttttttttttttttttttttttttttt');

        v_increase := employee_row.salary * NVL(employee_row.commission_pct, 0.02);

        debug_utils.log_variable('salary increase',TO_CHAR(v_increase));

        UPDATE employees
        SET salary = salary + v_increase
        WHERE employee_id = employee_row.employee_id;

        debug_utils.log($$PLSQL_UNIT, $$PLSQL_LINE, 'Salary updated for: ' || employee_row.full_name);
    END LOOP;

    debug_utils.log('ADJUST_SALARIES_BY_COMMISSION',  $$PLSQL_LINE, 'Procedure finished', 'INFO');

    COMMIT;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        debug_utils.log_error( $$PLSQL_UNIT, SQLERRM);
        RAISE;
END adjust_salaries_by_commission;
