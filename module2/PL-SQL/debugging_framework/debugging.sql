CREATE TABLE employees (
    employee_id    NUMBER PRIMARY KEY,
    full_name      VARCHAR2(100) NOT NULL,
    salary         NUMBER(10, 2) NOT NULL,
    commission_pct NUMBER(5, 4)
     CHECK (commission_pct BETWEEN 0 AND 1) -- let's say we can't offer more than double the salary
