CREATE OR REPLACE PACKAGE debug_utils AS
    g_debug_mode BOOLEAN := FALSE;

    PROCEDURE enable_debug;
    PROCEDURE disable_debug;
    PROCEDURE enable_level(p_level VARCHAR2);
    PROCEDURE disable_level(p_level VARCHAR2);

    PROCEDURE log(
        p_module_name VARCHAR2,
        p_line        NUMBER,
        p_message     VARCHAR2,
        p_level       VARCHAR2 DEFAULT 'INFO'
    );

    PROCEDURE log_msg(p_message VARCHAR2);
    PROCEDURE log_variable(p_name VARCHAR2, p_value VARCHAR2);
    PROCEDURE log_error(p_proc VARCHAR2, p_err VARCHAR2);
END debug_utils;
/


CREATE OR REPLACE PACKAGE BODY debug_utils AS

    g_debug_enabled BOOLEAN := FALSE;
    g_info_enabled  BOOLEAN := TRUE;
    g_warn_enabled  BOOLEAN := TRUE;
    g_error_enabled BOOLEAN := TRUE;

    PROCEDURE enable_debug IS
    BEGIN
        g_debug_mode := TRUE;
        g_debug_enabled := TRUE;
        g_info_enabled := TRUE;
        g_warn_enabled := TRUE;
        g_error_enabled := TRUE;
    END enable_debug;


    PROCEDURE disable_debug IS
    BEGIN
        g_debug_mode := FALSE;
    END disable_debug;


    PROCEDURE enable_level(p_level VARCHAR2) IS
        v_level VARCHAR2(10) := UPPER(p_level);
    BEGIN
        g_debug_mode := TRUE;

        CASE v_level
            WHEN 'DEBUG' THEN g_debug_enabled := TRUE;
            WHEN 'INFO'  THEN g_info_enabled := TRUE;
            WHEN 'WARN'  THEN g_warn_enabled := TRUE;
            WHEN 'ERROR' THEN g_error_enabled := TRUE;
            ELSE
                RAISE_APPLICATION_ERROR(-20001, 'Invalid log level: ' || UPPER(p_level));
        END CASE;
    END enable_level;


    PROCEDURE disable_level(p_level VARCHAR2) IS
        v_level VARCHAR2(10) := UPPER(p_level);
    BEGIN
        CASE v_level
            WHEN 'DEBUG' THEN g_debug_enabled := FALSE;
            WHEN 'INFO'  THEN g_info_enabled := FALSE;
            WHEN 'WARN'  THEN g_warn_enabled := FALSE;
            WHEN 'ERROR' THEN g_error_enabled := FALSE;
            ELSE
                RAISE_APPLICATION_ERROR(-20001, 'Invalid log level: ' || v_level);
        END CASE;
    END disable_level;


    PROCEDURE log(
        p_module_name VARCHAR2,
        p_line        NUMBER,
        p_message     VARCHAR2,
        p_level       VARCHAR2 DEFAULT 'INFO'
    ) IS
        v_level   VARCHAR2(10) := UPPER(NVL(p_level, 'INFO'));
        v_enabled BOOLEAN := FALSE;
    BEGIN
        CASE v_level
            WHEN 'DEBUG' THEN v_enabled := g_debug_enabled;
            WHEN 'INFO'  THEN v_enabled := g_info_enabled;
            WHEN 'WARN'  THEN v_enabled := g_warn_enabled;
            WHEN 'ERROR' THEN v_enabled := g_error_enabled;
            ELSE
                RAISE_APPLICATION_ERROR(-20001, 'Invalid log level: ' || v_level);
        END CASE;

        IF g_debug_mode AND v_enabled THEN
            INSERT INTO debug_log (
                module_name,
                line_no,
                log_level,
                log_message
            )
            VALUES (
                p_module_name,
                p_line,
                v_level,
                p_message
            );
        END IF;
    END log;


    PROCEDURE log_msg(p_message VARCHAR2) IS
    BEGIN
        log('MESSAGE', NULL, p_message, 'INFO');
    END log_msg;


    PROCEDURE log_variable(p_name VARCHAR2, p_value VARCHAR2) IS
    BEGIN
        log('VARIABLE', NULL, p_name || ' = ' || NVL(p_value, 'NULL'), 'DEBUG');
    END log_variable;


    PROCEDURE log_error(p_proc VARCHAR2, p_err VARCHAR2) IS
    BEGIN
        log(p_proc, NULL, 'ERROR: ' || p_err, 'ERROR');
    END log_error;

END debug_utils;
